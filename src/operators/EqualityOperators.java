// Equality Operators ->  ==, !=

package operators;

public class EqualityOperators {
    public static void main(String[] args) {
        System.out.println(4 == 5);
        System.out.println(true == false);
        System.out.println('a' == 10);
        System.out.println('a' != 'A');
    }
}

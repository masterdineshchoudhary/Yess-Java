package datatypes;

public class TypeCasting {
	public static void main(String[] args) {
		
		
		// type casted val1 from int to byte type
		int val1 = (byte) 127;
		System.out.println(val1);
		
		// type casted val2 from int to short type
		int val2 = (short) 128;
		System.out.println(val2);
		
		// type casted val3 from double to float type
		double val3 = (float) 128.0;
		System.out.println(val3);
		System.out.println(val3);	
	}
}
